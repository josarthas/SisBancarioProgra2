package Sucursal;


public class Sucursal {
    private String numSucursal;
    private String direccion;
    private String tipo;
    
    public sucursal(String num, String dire, String type){
        num=numSucursal;
        dire=direccion;
        type=tipo;
    
}

    public String getNumSucursal() {
        return numSucursal;
    }
    public String getDireccion() {
        return direccion;
    }
    public String getTipo() {
        return tipo;
    }

}
